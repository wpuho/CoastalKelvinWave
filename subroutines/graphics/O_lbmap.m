function map = O_lbmap(n,scheme)
% From /home/lameixisi/matlab/subroutine/lbmap.m
%LBMAP Returns specified Light-Bertlein colormap.
%
%   LBMAP(N,SCHEME) returns an Nx3 colormap. SCHEME can be one of the
%   following strings:
%
%       'Blue'       Single-hue progression to purlish-blue (default)
%       'BlueGray'   Diverging progression from blue to gray
%       'BrownBlue'  Orange-white-purple diverging scheme
%       'RedBlue'    Modified spectral scheme
%       'jrcmap'     Jingru's Colormap
%       'gmt_haxby'  Diverging color from blue, green yellow, red to whit
%
%   If N is not specified, the size of the colormap is determined by the
%   current figure. If no figure exists, MATLAB creates one.
%
%Example 1: 7-color single-hue blue (default)
%   load penny
%   imagesc(P)
%   colormap(lbmap(7))
%   colorbar
%
%Example 2: 11-color modified spectrum
%   load penny
%   imagesc(P)
%   colormap(lbmap(11,'RedBlue'))
%   colorbar
%
%   See also HSV, GRAY, HOT, BONE, COPPER, PINK, FLAG, COLORMAP, RGBPLOT.

% Reference:
% A. Light & P.J. Bartlein, "The End of the Rainbow? Color Schemes for
% Improved Data Graphics," Eos,Vol. 85, No. 40, 5 October 2004.
% http://geography.uoregon.edu/datagraphics/EOS/Light&Bartlein_EOS2004.pdf

% Copyright 2007-2010 The MathWorks, Inc.

%defensive programming
error(nargchk(0,2,nargin))
error(nargoutchk(0,1,nargout))

%defaults
if nargin<2
  scheme = 'Blue';
end
if nargin<1
  n = size(get(gcf,'colormap'),1);
end

%valid schemes
switch lower(scheme)
  case 'blue'
    baseMap = BlueMap;
  case 'bluegray'
    baseMap = BlueGrayMap;
  case 'brownblue'
    baseMap = BrownBlueMap;
  case 'redblue'
    baseMap = RedBlueMap;
  case 'redbluea'
    baseMap = RedBlueMapa;
  case 'redblue1'
    baseMap = RedBlueMap1;
  case 'redblue2'
    baseMap = RedBlueMap2;
  case 'rogermap3'
    baseMap = RogerMap3;
  case 'rogermap4'
    baseMap = RogerMap4;
  case 'bgyrp'
    baseMap = bgyrp;
  case 'edacmap2d'
    baseMap = edacmap2d;
  case 'edacmap2a'
    baseMap = edacmap2a;
  case 'edacmap2b'
    baseMap = edacmap2b;
  case 'greens'
    baseMap = greens;
  case 'greys'
    baseMap = greys;
  case 'terrain1'
    baseMap = terrain1;
  case 'terrain2'
    baseMap = terrain2;
  case 'ww3grads'
    baseMap = ww3grads;
  case 'jrcmap'
    baseMap = jrcmap;
  case 'gmt_haxby'
    baseMap = gmt_haxby;
  case 'bluenrednblue'
    baseMap = bluenrednblue;
  otherwise
    error(['Invalid scheme ' scheme])
end
idx1 = linspace(0,1,size(baseMap,1));
idx2 = linspace(0,1,n);
map = interp1(idx1,baseMap,idx2);

